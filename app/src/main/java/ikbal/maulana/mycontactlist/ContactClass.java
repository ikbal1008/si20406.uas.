public class ContactClass {
    private String nama, telepon, sosmed, alamat, foto;

    public String getNama() {
        return nama;
    }

    public String getTelepon() {
        return telepon;
    }

    public String getSosmed() {
        return sosmed;
    }

    public String getAlamat() {
        return alamat;
    }

    public String getFoto() {
        return foto;
    }
}
